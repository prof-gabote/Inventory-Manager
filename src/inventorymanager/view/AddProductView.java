    private void jButton_addProductActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_addProductActionPerformed
        Category c = new Category();
        String[] stringArray = jComboBox_productCategoryCmbBx.getSelectedItem().toString().split(" - ");
        int catId = Integer.parseInt(stringArray[0]);
        String catName = stringArray[1];
        
        Product p = new Product(Integer.parseInt(jTextField_productIdInput.getText()),
                jTextField_productNameInput.getText(),
                new Category(catId, catName),
                Integer.parseInt(jTextField_productPrice.getText()),
                Integer.parseInt(jTextField_productStock.getText()));
        
        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to add this product?", "Confirmation",JOptionPane.YES_NO_CANCEL_OPTION);
        if (option == 0)
        {
            pc.addProduct(p);
            JOptionPane.showMessageDialog(this, "Product added succesfully.", "Confirmation",JOptionPane.INFORMATION_MESSAGE);
        }
        this.dispose();
    }//GEN-LAST:event_jButton_addProductActionPerformed

