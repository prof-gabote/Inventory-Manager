    private void jButton_updateBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateBttnActionPerformed
        JTable t = jTable_viewTable;
        try {
            int row = t.getSelectedRow();

            Product p = new Product();
            p.setProductId(Integer.parseInt(t.getValueAt(row, 0).toString()));
            p.setProductName(t.getValueAt(row, 1).toString());

            String[] catAtrib = t.getValueAt(row, 2).toString().split(" - ");
            p.setProductCategory(new Category(Integer.parseInt(catAtrib[0]), catAtrib[1]));
            p.setProductPrice(Integer.parseInt(t.getValueAt(row, 3).toString()));
            p.setProductStock(Integer.parseInt(t.getValueAt(row, 4).toString()));

            UpdateProductView updateView = new UpdateProductView(this, true, p);

            updateView.setVisible(true);

            updateGui();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "You didn't choose a product.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_updateBttnActionPerformed

    private void jButton_deleteBttnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_deleteBttnActionPerformed
        JTable t = jTable_viewTable;
        int row = t.getSelectedRow();
        int id = Integer.parseInt(t.getValueAt(row, 0).toString());

        int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this product?", "Confirmation", JOptionPane.YES_NO_CANCEL_OPTION);

        if (option == 0) {
            pc.deleteProduct(id);
            updateGui();
            JOptionPane.showMessageDialog(this, "Product deleted succesfully.", "Confirmation", JOptionPane.INFORMATION_MESSAGE);
        }

    }//GEN-LAST:event_jButton_deleteBttnActionPerformed

