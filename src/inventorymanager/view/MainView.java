    //CUSTOM CODE
    private void updateGui() {

        List<Product> productList = pc.getAllProducts();

        DefaultTableModel model = (DefaultTableModel) jTable_viewTable.getModel();

        model.setNumRows(0);

        productList.forEach(
                x -> {
                    Object[] row = {
                        x.getProductId(),
                        x.getProductName(),
                        x.getProductCategory().getCategoryId() + " - " + x.getProductCategory().getCategoryName(),
                        x.getProductPrice(),
                        x.getProductStock()
                    };

                    model.addRow(row);
                }
        );

    }


