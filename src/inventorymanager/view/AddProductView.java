    private void updateComboBox() {
        String[] comboBoxModel = cc.getAllCategories()
                .stream()
                .map(x -> x.getCategoryId() + " - " + x.getCategoryName())
                .toArray(size -> new String[size]);

        ComboBoxModel<String> model = new DefaultComboBoxModel(comboBoxModel);
        jComboBox_productCategoryCmbBx.setModel(model);
    }


